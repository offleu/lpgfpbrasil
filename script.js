const menuToggle = document.querySelector(".menu-toggle");
const mobileNav = document.querySelector(".mobile-nav");
const mobileLinks = document.querySelectorAll(".mobile-nav a");
const revealItems = document.querySelectorAll(".reveal");
const parallaxTarget = document.querySelector("[data-parallax]");
const langButtons = document.querySelectorAll(".lang-btn");

const getValue = (source, path) =>
  path.split(".").reduce((current, key) => {
    if (current === undefined || current === null) return undefined;
    if (/^\d+$/.test(key)) return current[Number(key)];
    return current[key];
  }, source);

const applyLanguage = (lang) => {
  const copy = translations[lang] || translations.pt;

  document.documentElement.lang = copy.htmlLang;
  document.title = copy.pageTitle;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    const value = getValue(copy, key);
    if (typeof value === "string") node.textContent = value;
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((node) => {
    const rules = node.getAttribute("data-i18n-attr").split(",");
    rules.forEach((rule) => {
      const [attr, key] = rule.split(":");
      const value = getValue(copy, key);
      if (typeof value === "string") node.setAttribute(attr, value);
    });
  });

  if (menuToggle) {
    const expanded = menuToggle.getAttribute("aria-expanded") === "true";
    menuToggle.setAttribute("aria-label", expanded ? copy.menuAriaClose : copy.menuAriaOpen);
  }

  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });

  window.localStorage.setItem("gfp-language", lang);
};

if (menuToggle && mobileNav) {
  menuToggle.addEventListener("click", () => {
    const isOpen = mobileNav.classList.toggle("is-open");
    menuToggle.classList.toggle("is-open", isOpen);
    menuToggle.setAttribute("aria-expanded", String(isOpen));

    const currentLanguage = window.localStorage.getItem("gfp-language") || "pt";
    menuToggle.setAttribute(
      "aria-label",
      isOpen ? translations[currentLanguage].menuAriaClose : translations[currentLanguage].menuAriaOpen
    );
  });

  mobileLinks.forEach((link) => {
    link.addEventListener("click", () => {
      mobileNav.classList.remove("is-open");
      menuToggle.classList.remove("is-open");
      menuToggle.setAttribute("aria-expanded", "false");

      const currentLanguage = window.localStorage.getItem("gfp-language") || "pt";
      menuToggle.setAttribute("aria-label", translations[currentLanguage].menuAriaOpen);
    });
  });
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.lang || "pt");
  });
});

if ("IntersectionObserver" in window) {
  const revealObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          revealObserver.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.18 }
  );

  revealItems.forEach((item) => revealObserver.observe(item));
} else {
  revealItems.forEach((item) => item.classList.add("is-visible"));
}

const updateParallax = () => {
  if (!parallaxTarget) return;
  const offset = window.scrollY * 0.2;
  const opacity = Math.max(1 - window.scrollY / 700, 0.35);

  parallaxTarget.style.transform = `translateY(${offset}px)`;
  parallaxTarget.style.opacity = opacity.toFixed(3);
};

applyLanguage(window.localStorage.getItem("gfp-language") || "pt");
updateParallax();
window.addEventListener("scroll", updateParallax, { passive: true });
